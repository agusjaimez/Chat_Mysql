    private void registrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarseActionPerformed
 // TODO add your handling code here:
    if (this.user_field.getText() != null && this.pass.getText() != null && !"".equals(user_field.getText())){
        if(this.pass_verify.getText().equals(this.pass.getText())){
        try{
            if (conexion.registro(conexion.obtener(), this.user_field.getText(), this.pass.getText())==false){
            this.lbl_user.setVisible(true);
        }else{
            dispose();}
        } catch (SQLException | NullPointerException | ClassNotFoundException ex) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }else{
        this.lbl_pass.setVisible(true);
    }}else{
    this.lbl_comp.setVisible(true);}
    }//GEN-LAST:event_registrarseActionPerformed

